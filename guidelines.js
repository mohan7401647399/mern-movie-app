// React Movie Database Task

// Objective:

// Create a movie database web application using React.js that fetches movie data from the OMDB API and allows users to search for movies, view details, and add favorites.

//     Requirements:

// Homepage:

// Display a search bar where users can search for movies by title.
// Display a list of search results below the search bar.
// Each search result should display the movie title, year of release, and poster image.


// Movie Details Page:

// When a user clicks on a search result, they should be taken to a page that displays detailed information about the movie.
// Display the movie title, year of release, plot summary, runtime, genre, director, and poster image.
// Include a button to add the movie to favorites.


// Favorites Page:

// Display a list of favorite movies.
// Each favorite movie should display the movie title, year of release, and poster image.
// Allow users to remove movies from their favorites list.


// State Management:

// Use React Hooks for state management.
// Use Redux or Context API for managing global state, if necessary.


//     Routing:

// Implement React Router for navigation between pages(homepage, movie details page, favorites page).


// OMDB API Integration:

// Fetch movie data from the OMDB API(http://www.omdbapi.com/) using Axios or Fetch.
//         Display search results and movie details based on data retrieved from the API.


//         Styling:

//         Apply CSS styles to make the application visually appealing.
// Use responsive design principles to ensure the application works well on different screen sizes.



//         Bonus:

//         Authentication:

//         Implement user authentication using JWT or Firebase Authentication.
// Allow users to sign up, log in, and log out.


//         Pagination:

//         Implement pagination for search results to improve performance and user experience.


// Error Handling:

// Implement error handling for failed API requests and display error messages to users.


// Local Storage:

// Store favorite movies in the browser's local storage so they persist between sessions.


// Unit Tests:

// Write unit tests using Jest and React Testing Library to ensure components behave as expected.
